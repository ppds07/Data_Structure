#LIST
l1 = ['A','B',100,200]
l2 = ['a','b','c']
print("List 1 is:",l1)
print("List 2 is:",l2)
#To access a particular element in the list
print("Particular element in the list is:",l1[3])
print("Particular range of element in the list is:",l1[0:2])
#Delete the element in the list
del l1[1]
print("List after deletion:",l1)
#Updating the elements in the list
l1[2] = 99
print("List after updating:",l2)
